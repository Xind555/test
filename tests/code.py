"""Math class"""

@classmethod
class Math():
	
	@staticmethod
	def sum(var1, var2):
		"""sum"""
		return var1 + var2

	@staticmethod
	def subs(var1, var2):
		"""subs"""
		return var1 - var2

	@staticmethod
	def mul(var1, var2):
		"""mul"""
		return var1 * var2
	